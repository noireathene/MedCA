package com.example.medca.models;

public class booked {

    public String bookdate,booktime,bookstatus,bookreason;

    public booked() {
    }


    public String getBookdate() {
        return bookdate;
    }

    public void setBookdate(String bookdate) {
        this.bookdate = bookdate;
    }

    public String getBooktime() {
        return booktime;
    }

    public void setBooktime (String booktime) {
        this.booktime = booktime;
    }

    public String getBookstatus() {
        return bookstatus;
    }

    public void setBookstatus(String bookstatus) { this.bookstatus = bookstatus; }

    public String getBookreason() {
        return bookreason;
    }

    public void setBookreason (String bookreason) { this.bookreason = bookreason; }



}
